	<h2 class="text-danger">Kassenverwaltung vom Büro aus</h2>
	<p>Mit dem Back-Office-Modul verwalten Sie Kassen im Netzwerk ganz unproblematisch und bequem von Ihrem Büro aus. Im Wesentlichen entspricht das Back-Office-Modul in Funktionalität und Umfang der Chef / Oberkellnerebene von <strong>BONiFlex</strong>. Mit dem Back-Office-Modul können Sie </p>
		<ul>
			<li>Artikel, Artikelvarianten, Kellner, Warengruppen, Tische, Lagerwaren und Kunden anlegen und pflegen.</li>	
			<li>Rezepturen und Bemerkungen zu Artikeln hinterlegen.</li>
			<li>Serienbriefe zu Werbezwecken durch die Exportfunktion der Kundendatei erstellen</li>
			<li>offene Posten verwalten und Eingangszahlungen buchen.</li>
			<li>Berichte drucken.</li>  
			<li>Auswertungen  vornehmen.</li> 
			<li>das Journal durch den integrierten Journalreport auswerten und das Belegarchiv zur Kontrolle einsehen.</li>
			<li>festlegen welche Artikel auf dem Artikeltouchpad angezeigt werden sollen.</li>
			<li>das Erscheinungsbild der Kasse ändern, indem Sie Buttons anlegen oder ändern.</li>
			<li>das Lager verwalten</li>
			<li>Tischreservierungen vornehmen und bearbeiten</li>
			<li>Happy-Hour-Zeiträume anlegen.</li>
			<li>Back-Office-Modul Gastrokasse BONjour Gast</li>
		</ul>
	<p>	Alle Änderungen, die Sie über das Back-Office-Modul vornehmen, werden unmittelbar für alle im Netzwerk installierten Kassen gültig. Wenn Sie beispielsweise einen neuen Artikel anlegen, steht dieser Artikel sofort an allen verbundenen Kassen zur Verfügung.</p>
